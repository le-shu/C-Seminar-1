void PrintImage (int[,] img)
{
    for (int rows = 0; rows < img.GetLength(0); rows++)
    {
        for (int columns = 0; columns < img.GetLength(1); columns++)
        {
            if (img[rows, columns] == 0)
            {
                Console.Write($" ");
            }
            else
            {
                Console.Write($"+");
            }
        }
    Console.WriteLine();    
    }
}

